// Get a translated version of a plant entry
export function getTranslatedPlant(plant: PlantDBEntry): PlantDBEntry {
  const t = i18n.t.bind(i18n);
  const key = plant.id;
  return {
    ...plant,
    name: t(`${key}.name`, { ns: 'plants', defaultValue: plant.name }),
    tip: t(`${key}.tip`, { ns: 'plants', defaultValue: plant.tip }),
    description: t(`${key}.description`, { ns: 'plants', defaultValue: plant.description }),
    problems: (t(`${key}.problems`, { ns: 'plants', returnObjects: true, defaultValue: plant.problems }) as typeof plant.problems),
  };
}

// Get the full translated plant database
export function getTranslatedDatabase(): PlantDBEntry[] {
  return PLANT_DATABASE.map(getTranslatedPlant);
}

// Helper function to get plants by category
export function getPlantsByCategory(category: PlantCategory): PlantDBEntry[] {
  return PLANT_DATABASE.filter(plant => plant.category === category);
}

// Helper function to search plants by name (searches both original and translated)
export function searchPlants(query: string): PlantDBEntry[] {
  const lowerQuery = query.toLowerCase();
  return PLANT_DATABASE.filter(
    plant => {
      const translated = getTranslatedPlant(plant);
      return translated.name.toLowerCase().includes(lowerQuery) ||
        plant.scientificName.toLowerCase().includes(lowerQuery) ||
        plant.name.toLowerCase().includes(lowerQuery);
    }
  );
}

// Helper function to get a plant by ID
export function getPlantById(id: string): PlantDBEntry | undefined {
  return PLANT_DATABASE.find(plant => plant.id === id);
}
